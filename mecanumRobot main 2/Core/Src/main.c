void InitRobot(void) {
	uint8_t addr;
	switch (ROBOT_ID) {
	case 1:
		addr = SLAVE_1_ADDR;
		break;
	case 2:
		addr = SLAVE_2_ADDR;
		break;
	case 3:
		addr = SLAVE_3_ADDR;
		break;
	default:
		printf("ERROR: Invalid ROBOT_ID %d\n", ROBOT_ID);
		return;
	}

	if (HAL_I2C_Init(&hi2c3) != HAL_OK) {
		printf("ERROR: I2C3 init failed\n");
		return;
	}

	if (HAL_I2C_EnableListen_IT(&hi2c3) != HAL_OK) {
		printf("ERROR: I2C3 listen failed\n");
		return;
	}

	last_move_time = HAL_GetTick();
	printf("Robot %d ready (I2C3: 0x%02X)\n", ROBOT_ID, addr);
}

void ProcessMovement(void) {
	if (new_movement) {
		new_movement = 0;
		last_move_time = HAL_GetTick();

		if (received_move.command == 1) {
			MoveToPosition(received_move.x, received_move.y,
					received_move.theta, received_move.speed);
			isMoving = (received_move.x != 0 || received_move.y != 0
					|| received_move.theta != 0);
			printf("Move: vx=%.2f vy=%.2f omega=%.2f\n",received_move.x,
					received_move.y, received_move.theta);
		} else {
			MoveToPosition(0, 0, 0, 0);
			isMoving = 0;
			printf("Stop\n");
		}
	}

	// Safety timeout
	if (HAL_GetTick() - last_move_time > 1500) {
		if (isMoving) {
			MoveToPosition(0, 0, 0, 0);
			isMoving = 0;
			printf("Timeout stop\n");
		}
	}
}

// I2C3 callbacks
void HAL_I2C_AddrCallback(I2C_HandleTypeDef *hi2c, uint8_t TransferDirection,
		uint16_t AddrMatchCode) {
	if (hi2c->Instance == I2C3 && TransferDirection == I2C_DIRECTION_TRANSMIT) {
		HAL_I2C_Slave_Seq_Receive_IT(&hi2c3, (uint8_t*) &received_move,
				sizeof(movement_command_t), I2C_FIRST_AND_LAST_FRAME);
	}
}

void HAL_I2C_SlaveRxCpltCallback(I2C_HandleTypeDef *hi2c) {
	if (hi2c->Instance == I2C3) {
		new_movement = 1;
		printf("Command received\n");
		HAL_I2C_EnableListen_IT(&hi2c3);
		ProcessMovement();
	}
}

void HAL_I2C_ErrorCallback(I2C_HandleTypeDef *hi2c) {
	if (hi2c->Instance == I2C3) {
		printf("I2C3 Error: 0x%08X\n", (unsigned int) hi2c->ErrorCode);
		HAL_I2C_DeInit(hi2c);
		HAL_Delay(10);
		HAL_I2C_Init(hi2c);
		HAL_I2C_EnableListen_IT(&hi2c3);
	}
}

void HAL_I2C_ListenCpltCallback(I2C_HandleTypeDef *hi2c) {
	if (hi2c->Instance == I2C3) {
		HAL_I2C_EnableListen_IT(&hi2c3);
	}
}
